    private void exibirTabela() {
        TxtPercentualConcluido.setText(Integer.toString(tarefa.getPercentualConcluido()));
        DefaultTableModel tblItem = (DefaultTableModel) TblItem.getModel();
        int qtdLinhas = tblItem.getRowCount();

        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tblItem.removeRow(0);
        }
        for (Item novoItem : tarefa.getItens()) {
            Object linha[] = {novoItem,  novoItem.getDataExecucao(), novoItem.getPercentualCorrespondente()};
            tblItem.addRow(linha);
        }
    }
    
