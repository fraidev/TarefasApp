    private void BtnTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTarefaActionPerformed
        // TODO add your handling code here:
        if (!alterar) {
            tarefa = new Tarefa();
            TxtPercentualConcluido.setText(Integer.toString(tarefa.getPercentualConcluido()));
        }
        tarefa.setDetalhamento(txtDetalhamento.getText());
        tarefa.setNome(TxtNome.getText());

        //if(tarefa.getStatus() == EnumStatusTarefa.concluida){
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            tarefa.setDataLimite(LocalDate.parse(TxtData.getText(), formatter));
        //}
    }//GEN-LAST:event_BtnTarefaActionPerformed

    private void BtnNovoItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnNovoItemActionPerformed
        // TODO add your handling code here:
        GuiItem g = new GuiItem(null, true);
        g.setVisible(true);
        g.setAlterar(false);
        tarefa.addItem(g.getItem());
        exibirTabela();
    }//GEN-LAST:event_BtnNovoItemActionPerformed

    private void BtnAlterarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAlterarItemActionPerformed
        // TODO add your handling code here:
        Item item = (Item) TblItem.getValueAt(TblItem.getSelectedRow(), 0);
        GuiItem g = new GuiItem(null, true);
        g.setItem(item);
        g.setAlterar(true);
        g.setVisible(true);
        tarefa.setPercentualConcluido(item.getPercentualCorrespondente());
        exibirTabela();
    }//GEN-LAST:event_BtnAlterarItemActionPerformed

    private void BtnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverItemActionPerformed
        // TODO add your handling code here:
        Item item = (Item) TblItem.getValueAt(TblItem.getSelectedRow(), 0);
        tarefa.removeItem(item);
        exibirTabela();
    }//GEN-LAST:event_BtnRemoverItemActionPerformed

